#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*elem;

	if (!(elem = ft_memalloc(sizeof(t_list))))
		return (NULL);
	if (content == NULL)
	{
		elem->content = NULL;
		elem->content_size = 0;
	}
	else
	{
		elem->content = ft_memdup(content, content_size);
		elem->content_size = content_size;
	}
	elem->next = NULL;
	return (elem);
}
