int		ft_getnbword_max(char *s, int max)
{
	int		nb;
	int		new;

	nb = 0;
	new = 1;
	while (*s)
	{
		if (*s == ' ')
			new = 1;
		else if (new)
		{
			new = 0;
			nb++;
		}
		s++;
		if (nb >= max)
			return (-1);
	}
	return (nb);
}
