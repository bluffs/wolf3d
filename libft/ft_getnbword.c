int		ft_getnbword(char *s)
{
	int		nb;
	int		new;

	nb = 0;
	new = 1;
	while (*s)
	{
		if (*s == ' ')
			new = 1;
		else if (new)
		{
			new = 0;
			nb++;
		}
		s++;
	}
	return (nb);
}
