#include "libft.h"

int		ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	nb;

	nb = 0;
	neg = 1;
	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '+' || str[i] == '-')
		neg = (str[i++] == '-') ? -1 : 1;
	while (str[i] >= '0' && str[i] <= '9')
		nb = nb * 10 + str[i++] - '0';
	return (neg * nb);
}
